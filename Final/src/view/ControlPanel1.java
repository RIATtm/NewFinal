    public void timer() {
        Timer t;
        t = new Timer(0, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date d = new Date();
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd hh:mm:ss a");
                time.setText(sdf.format(d));
            }
        });
        t.start();
    }

