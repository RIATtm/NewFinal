    public void disableTextFields() {
        int getCurPasNo = new String(txt_currPas.getPassword()).length();
        if (getCurPasNo < 1) {
            txt_newPas.setVisible(false);
            txt_confirmPas.setVisible(false);
        } else {
            txt_newPas.setVisible(true);
            txt_confirmPas.setVisible(true);
        }
    }

    //------------otherAccounts-------------------------------------------------
    public void getOtherAccounts() {
        try {
            ResultSet rs = DBConnection.setConnection().createStatement().executeQuery("SELECT u.user_type,username FROM userType u,login l WHERE u.user_nic=l.userType_user_nic");
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("username"));
                v.add(rs.getString("user_type"));
                dt.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

