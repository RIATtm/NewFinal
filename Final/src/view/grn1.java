    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed

        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        Vector v = new Vector();
        int i = dt.getRowCount();
        i++;
        v.add(i);
        v.add(combo_item.getSelectedItem());
        v.add(txt_quantity.getText());
        int qu = Integer.parseInt(txt_quantity.getText());
        String id = combo_item.getSelectedItem().toString().split(" ")[0];
        try {
            ResultSet rs = DBConnection.setConnection().createStatement().executeQuery("SELECT pro_unitPrice FROM product WHERE pro_id='" + id + "'");
            while (rs.next()) {
                v.add(rs.getString("pro_unitPrice"));
                price = Integer.parseInt(rs.getString("pro_unitPrice"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        v.add(qu * price);
        //-----------------------------------------------------------------

        double tot = 0;
        for (int a = 0; a < dt.getRowCount(); a++) {
            double d = Double.parseDouble(dt.getValueAt(a, 5).toString());
            tot += d;

        }
        txt_grandTotal.setText(tot + "");

    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String autoIncremtent = null;
        try {
            DBConnection.setConnection().createStatement().executeUpdate("INSERT INTO grn VALUES('" + autoIncremtent + "','" + txt_cashPayed.getText() + "','" + txt_grandTotal.getText() + "','" + txt_balance.getText() + "','" + timer.getText() + "','" + txt_supplier.getText() + "')");
            DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
            for (int aa = 0; aa < dt.getRowCount(); aa++) {
                String no = dt.getValueAt(aa, 0).toString();
                String pidSplit = dt.getValueAt(aa, 1).toString().split(" ")[0];
                String quantity = dt.getValueAt(aa, 0).toString();
                String price = dt.getValueAt(aa, 0).toString();
                String total = dt.getValueAt(aa, 0).toString();
                String p_id = dt.getValueAt(aa, 0).toString();
                DBConnection.setConnection().createStatement().executeUpdate("INSERT INTO grn_reg VALUES('"+autoIncremtent+"','"+no+"','"+pidSplit+"','"+quantity+"','"+price+"','"+total+"')");
            
                //  stock update
                ResultSet rs=DBConnection.setConnection().createStatement().executeQuery("SELECT pro_quantity FROM stock WHERE product_pro_id='"+pidSplit+"'");
                if(rs.next()){
                    int qu=Integer.parseInt(rs.getString("pro_quantity"));
                    int newQu=Integer.parseInt(txt_quantity.getText());
                    int totQu=qu+newQu;
                    DBConnection.setConnection().createStatement().executeUpdate("UPDATE stock set pro_quantity='"+totQu+"' WHERE product_pro_id='"+pidSplit+"'");
                    JOptionPane.showMessageDialog(this, "Updated succesfully");
                }else{
                    DBConnection.setConnection().createStatement().executeUpdate("INSERT INTO stock VALUES('"+timer.getText()+"','"+quantity+"','"+pidSplit+"')");
                    JOptionPane.showMessageDialog(this, "Addeed succesfully");
                }
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton6ActionPerformed

